// จงหาค่ามากสุดและน้อยสุดใน arr
// ```js
// let arr = [3.24, 2.78, 3.86, 1.37, 0.52];
// ```

// Max
let arr1 = [3.24, 2.78, 3.86, 1.37, 0.52];
const max = Math.max(...arr1);

console.log(max); // 3.86

// Min
let arr2 = [3.24, 2.78, 3.86, 1.37, 0.52];
const min = Math.min(...arr2);

console.log(min); // 0.52



